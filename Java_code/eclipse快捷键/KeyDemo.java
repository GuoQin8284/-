package com.demo1;

/*
 *内容辅助键：alt+/
 *	A：main方法
 *		main，然后alt+/，回车
 *	B：输入语句
 *		syso，然后alt+/，回车 
 *
 *快捷键：
 *	A：注释
 *		单行 选中内容，ctrl+/，在按一次就取消注释
 *		多行 选中内容，ctrl+shift+/，在按一次ctrl+shift+\就取消注释
 *	B:格式化
 *		ctrl+shitf+f
 *		或者 右键--source --Format
 *
 */
public class KeyDemo {
	public static void main(String[] args) {
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
	}
}
